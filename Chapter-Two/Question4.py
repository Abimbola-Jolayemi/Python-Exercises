num1 = 27.5
num2 = 2

num_add = num1 + num2
num_subtract = num1 - num2
num_multiply = num1 * num2
num_quotient = num1 / num2
num_floor_quotient = num1 // num2
num_exponent = num1 ** num2

print("The value of", num1, "+", num2, "is", num_add)
print("The value of", num1, "-", num2, "is", num_subtract)
print("The value of", num1, "*", num2, "is", num_multiply)
print("The value of", num1, "/", num2, "is", num_quotient)
print("The value of", num1, "//", num2, "is", num_floor_quotient)
print("The value of", num1, "exponent", num2, "is", num_exponent)